# Dictionary in Python example

# 'marks' is a dictionary where keys are student names and values are their marks
marks = {
    "Sunny": 98,      # Key = "Sunny", Value = 98
    "Rohan": 74,      # Key = "Rohan", Value = 74
    "Chandan": 76     # Key = "Chandan", Value = 76
}

# Printing the dictionary and its type
print(marks, type(marks))  
# Output: {'Sunny': 98, 'Rohan': 74, 'Chandan': 76} <class 'dict'>
